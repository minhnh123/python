import math

a = int(input("Nhập số nguyên a: "))
b = int(input("Nhập số nguyên b: "))

print("Tổng của a và b:", a + b)
print("Hiệu của a và b:", a - b)
print("Tích của a và b:", a * b)
print("Thương của a chia cho b:", a / b)
print("Phần còn lại khi a chia cho b:", a % b)
print("Kết quả của log10(a):", math.log10(a))
print("Kết quả của a^b:", a ** b)